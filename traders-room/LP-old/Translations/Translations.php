function GetFormTranslation($alias) 
{
	$_lang = getPageLanguage();

	 if($_lang == 'en-uk') {
		$_lang = 'en';
	}	

	$form_translations = GetFormTranslationsArray();
	$key = $_lang.'_'.$alias;
	
	if (array_key_exists($key, $form_translations)) {
		return $form_translations[$key];
	}
	
	return $key;
}
