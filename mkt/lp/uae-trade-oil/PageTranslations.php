function GetPageTranslation($alias) {

    $lang_code = getPageLanguage();
    $page_translations = GetPageTranslationsArray();

    $key = $lang_code.'_'.$alias;

    if (array_key_exists($key, $page_translations)) {
        return $page_translations[$key];
    }

    return $key;
}

function getAsset($assetName){

    $selectedItem = "";
    $popInst = new PopularInstruments();

    //Array of PopularInstrumentsModel
    $data = $popInst->GetPopularInstruments();

    foreach ($data as $item){
        if($item->TickerName == $assetName){
            $selectedItem = $item;
        }
    }


    $old_price = 50;
    $assets_buy = $selectedItem->Buy;
    $assets_sell = $selectedItem->Sell;
    $assets_price = $selectedItem->Price;
    $change = $selectedItem->PriceChange;
    $direction = '+';
    if ($assets_price - $old_price < 0){
        $direction = '';
    }


    $row= array("change"=>$change, "price"=>$selectedItem->Price, "buy"=>$selectedItem->Buy, "sell"=>$selectedItem->Sell, "direction"=> $direction);

    return $row;

}


