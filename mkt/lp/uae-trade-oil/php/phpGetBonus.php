function getAwords($countryCode){
    $countries = availableCountries();
    $len = count($countries);
    for ($i = 0; $i < $len; $i++) {	   
        $val = $countries[$i];
	   
        if ($val['code'] === $countryCode) {
            return TRUE;
        }
   }
    return FALSE;
}
