function getTagSize($number){
    $size = strlen($number);
    if ($size < 5) {
        $tag_class = "price-stick";
    } else if ($size == 5) {
        $tag_class = "price-stick-1";
    } else if ($size == 6) {
        $tag_class = "price-stick-2";
