function getPrice($asset){
    $selectedItem = "";
    $popInst = new PopularInstruments();

    //Array of PopularInstrumentsModel
    $data = $popInst->GetPopularInstruments();

    foreach ($data as $item){
        if($item->TickerName == $asset){
            $selectedItem = $item;
        }
    }
    $change_dir = 'up';
    $change = $selectedItem->PriceChange;

    if (strpos($change, '-') !== false) {
        $change_dir = 'down';
    }
    return array($selectedItem->Price, $change_dir, round($change, 3));
}

